document.addEventListener("DOMContentLoaded", () => {
    const userForm = document.querySelector("form");
    const listContainer = document.querySelector(".data-list-wrapper");

    // ==========================================
    // 1. EXECUTION BLOCK: CREATE USER VIEW LOGIC
    // ==========================================
    if (userForm && window.location.pathname.includes("create-user")) {
        userForm.addEventListener("submit", async (e) => {
            e.preventDefault();
            
            const usernameInput = userForm.querySelector('[name="username"]') || userForm.querySelector('input[type="text"]');
            const emailInput = userForm.querySelector('[name="email"]');
            const roleSelect = userForm.querySelector('select');
            
            const userData = {
                username: usernameInput.value,
                email: emailInput ? emailInput.value : `${usernameInput.value}@saqr.edu`,
                password: "password123", 
                role: roleSelect ? roleSelect.value : "student"
            };

            if (!userData.username) {
                alert("Please enter a username.");
                return;
            }

            try {
                await DataService.createUser(userData);
            } catch (error) {
                alert(error.message);
                return;
            }
            
            // Write system logs and dispatch notification alerts
            SystemLogEngine.write(`New User Registered: ${userData.username} mapped to access level role [${userData.role}]`, "SECURITY");
            NotificationEngine.dispatch("admin", "all", `Alert: New profile platform account generated by ${userData.username}.`);

            alert(`Profile Record for ${userData.username} (${userData.role}) has been deployed successfully!`);
            userForm.reset();
        });
    }

    // ==============================================
    // 2. EXECUTION BLOCK: MANAGE STUDENTS VIEW LOGIC
    // ==============================================
    if (listContainer && window.location.pathname.includes("manage-students")) {
        const renderStudentsList = async () => {
            const headerHTML = listContainer.querySelector(".data-list-header").outerHTML;
            const students = await DataService.getUsersByRole("student");
            
            let structuralRows = "";
            students.forEach(student => {
                structuralRows += `
                    <div class="data-list-row" data-id="${student.id}">
                        <div>${student.username}</div>
                        <div>${student.email}</div>
                        <div>System Provisioned</div>
                        <div>Active Status</div>
                        <div class="action-icon-group">
                            <i class="fa-solid fa-user-pen" title="Edit Profile"></i>
                            <i class="fa-solid fa-trash-can style-delete-btn" title="Delete User Record" style="color:#ef4444;"></i>
                        </div>
                    </div>`;
            });
            
            listContainer.innerHTML = headerHTML + structuralRows;
            attachRowActionListeners();
        };

        const attachRowActionListeners = () => {
            listContainer.querySelectorAll(".style-delete-btn").forEach(btn => {
                btn.addEventListener("click", (e) => {
                    const targetRow = e.target.closest(".data-list-row");
                    const targetId = targetRow.getAttribute("data-id");
                    
                    if (confirm("Are you sure you want to delete this student record?")) {
                        DataService.deleteUser(targetId);
                        targetRow.remove();
                    }
                });
            });
        };

        renderStudentsList();
    }

    // ===============================================
    // 3. EXECUTION BLOCK: MANAGE LECTURERS VIEW LOGIC
    // ===============================================
    if (listContainer && window.location.pathname.includes("manage-lecturers")) {
        const renderLecturersList = async () => {
            const headerHTML = listContainer.querySelector(".data-list-header").outerHTML;
            const lecturers = await DataService.getUsersByRole("lecturer");
            
            let structuralRows = "";
            lecturers.forEach(lecturer => {
                structuralRows += `
                    <div class="data-list-row" data-id="${lecturer.id}">
                        <div>${lecturer.username}</div>
                        <div>${lecturer.email}</div>
                        <div>Faculty Staff</div>
                        <div>Active Status</div>
                        <div class="action-icon-group">
                            <i class="fa-solid fa-user-pen" title="Edit Profile"></i>
                            <i class="fa-solid fa-trash-can style-delete-btn" title="Delete Lecturer Record" style="color:#ef4444;"></i>
                        </div>
                    </div>`;
            });
            
            listContainer.innerHTML = headerHTML + structuralRows;
            attachLecturerActionListeners();
        };

        const attachLecturerActionListeners = () => {
            listContainer.querySelectorAll(".style-delete-btn").forEach(btn => {
                btn.addEventListener("click", (e) => {
                    const targetRow = e.target.closest(".data-list-row");
                    const targetId = targetRow.getAttribute("data-id");
                    
                    if (confirm("Are you sure you want to permanently delete this lecturer profile?")) {
                        DataService.deleteUser(targetId);
                        targetRow.remove();
                    }
                });
            });
        };

        renderLecturersList();
    }

    // ===============================================
    // 4. EXECUTION BLOCK: MANAGE SUBJECTS VIEW LOGIC
    // ===============================================
    if (window.location.pathname.includes("manage-subject")) {
        
        // 4A. STANDALONE DROPDOWN POPULATION
        // Safely processes drop-downs independently from data table presence
        const lecturerDropdown = document.querySelector('select') || document.querySelector('.form-control select');
        if (lecturerDropdown) {
            const activeUsers = MockDB.getItems('saqr_users') || [];
            const filteredLecturers = activeUsers.filter(u => u.role === "lecturer");
            
            lecturerDropdown.innerHTML = '<option value="">-- Select Assignee Faculty Member --</option>';
            filteredLecturers.forEach(lec => {
                const opt = document.createElement('option');
                opt.value = lec.username;
                opt.textContent = lec.username;
                lecturerDropdown.appendChild(opt);
            });
        }

        // 4B. DATA LIST MATRIX MANAGEMENT
        // Renders visual data items only if the container exists on screen
        const subjectMonitor = document.getElementById("subject-inventory-monitor-target");
        const renderSubjectRows = (subjects) => subjects.map(subject => `
            <div class="data-list-row" data-id="${subject.id}">
                <div><strong>${subject.name}</strong></div>
                <div>${subject.code}</div>
                <div>Assigned to: <strong>${subject.assignedLecturer || "Unassigned"}</strong></div>
                <div>${subject.credits || "0"} Credits</div>
                <div class="action-icon-group">
                    <i class="fa-solid fa-sliders" title="Subject settings"></i>
                    <i class="fa-solid fa-trash-can style-delete-btn" title="Delete subject" style="color:#ef4444;"></i>
                </div>
            </div>`).join("");

        const attachSubjectDeleteListeners = (container) => {
            container.querySelectorAll(".style-delete-btn").forEach(btn => {
                btn.addEventListener("click", async (e) => {
                    const targetRow = e.target.closest(".data-list-row");
                    const targetId = targetRow.getAttribute("data-id");
                    if (confirm("Remove this course assignment?")) {
                        await DataService.deleteSubject(targetId);
                        targetRow.remove();
                        SystemLogEngine.write(`Subject curriculum record removed by admin`, "INFO");
                    }
                });
            });
        };

        if (listContainer) {
            const renderSubjectsList = async () => {
                const headerHTML = listContainer.querySelector(".data-list-header").outerHTML;
                const subjects = await DataService.getSubjects();
                listContainer.innerHTML = headerHTML + renderSubjectRows(subjects);
                attachSubjectDeleteListeners(listContainer);
            };

            renderSubjectsList();
        }

        if (subjectMonitor) {
            const renderSubjectMonitor = async () => {
                const subjects = await DataService.getSubjects();
                subjectMonitor.innerHTML = subjects.length === 0
                    ? `<div style="padding:20px; color:#64748b;">No subjects created yet.</div>`
                    : `<div class="data-list-wrapper">
                        <div class="data-list-header">
                            <div>Subject</div><div>Code</div><div>Lecturer</div><div>Credits</div><div>Actions</div>
                        </div>
                        ${renderSubjectRows(subjects)}
                    </div>`;
                attachSubjectDeleteListeners(subjectMonitor);
            };
            renderSubjectMonitor();
        }

        if (userForm && window.location.pathname.includes("manage-subject")) {
            userForm.addEventListener("submit", async (e) => {
                e.preventDefault();

                const subjectCode = userForm.querySelector('[name="subject-code"]')?.value.trim();
                const subjectName = userForm.querySelector('[name="subject-name"]')?.value.trim();
                const credits = userForm.querySelector('[name="subject-credits"]')?.value.trim();
                const lecturer = userForm.querySelector('[name="subject-lecturer-id"]')?.value.trim();

                if (!subjectCode || !subjectName || !lecturer) {
                    alert("Please fill in subject code, subject name, and lecturer.");
                    return;
                }

                const createdSubject = await DataService.createSubject({
                    name: subjectName,
                    code: subjectCode,
                    credits: credits || "0",
                    department: "Academic Branch",
                    assignedLecturer: lecturer
                });

                SystemLogEngine.write(`Admin assigned course curriculum code [${createdSubject.code}] to faculty lead: ${createdSubject.assignedLecturer}`, "INFO");
                NotificationEngine.dispatch("lecturer", createdSubject.assignedLecturer, `Notice: Course assignment update. You have been assigned to lead ${createdSubject.name}.`);

                alert(`Subject ${createdSubject.code} successfully assigned!`);
                userForm.reset();
                if (subjectMonitor) subjectMonitor.innerHTML = "";
                if (subjectMonitor) {
                    const subjects = await DataService.getSubjects();
                    subjectMonitor.innerHTML = `<div class="data-list-wrapper">
                        <div class="data-list-header">
                            <div>Subject</div><div>Code</div><div>Lecturer</div><div>Credits</div><div>Actions</div>
                        </div>
                        ${renderSubjectRows(subjects)}
                    </div>`;
                    attachSubjectDeleteListeners(subjectMonitor);
                }
            });
        }
    }
});

