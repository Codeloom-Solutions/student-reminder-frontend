document.addEventListener("DOMContentLoaded", () => {
    const registerForm = document.querySelector("form");
    if (!registerForm) return;

    registerForm.addEventListener("submit", async (e) => {
        e.preventDefault();

        // Target form elements safely using typical input selections
        const usernameInput = registerForm.querySelector('[name="username"]') || registerForm.querySelector('[name="fullName"]') || registerForm.querySelector('input[type="text"]');
        const emailInput = registerForm.querySelector('[name="email"]') || registerForm.querySelector('input[type="email"]');
        const passwordInput = registerForm.querySelector('[name="password"]') || registerForm.querySelector('input[type="password"]');
        const confirmInput = registerForm.querySelector('[name="confirmPassword"]');

        if (!usernameInput.value || !emailInput.value || !passwordInput.value) {
            alert("Please complete all registration fields.");
            return;
        }

        if (confirmInput && passwordInput.value !== confirmInput.value) {
            alert("Passwords do not match.");
            return;
        }

        const targetRole = "student";

        const userData = {
            username: usernameInput.value.trim(),
            email: emailInput.value.trim(),
            password: passwordInput.value,
            role: targetRole
        };

        // Persist into Core Data Service Layer
        try {
            await DataService.createUser(userData);
        } catch (error) {
            alert(error.message);
            return;
        }
        
        // Write system logs and dispatch notification alerts
        SystemLogEngine.write(`New User Registered: ${userData.username} mapped to access level role [${userData.role}]`, "SECURITY");
        NotificationEngine.dispatch("admin", "all", `Alert: New profile platform account generated by ${userData.username}.`);
        
        alert(`Account created successfully as a ${targetRole.toUpperCase()}! Redirecting to login...`);
        window.location.href = "../Public Pages/login.html";
    });
});
